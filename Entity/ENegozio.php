<?php


class ENegozio extends EUtente_loggato{
     // email Ã¨ identificativo e fa riferimento a email della classe padre
     private $email_negozio;
     private $nameShop;
     private $p_iva;
     private $andress;
     private $card;
     private $abbonamento;

       public function __construct($name, $mail, $pw, $tel, $stato, $datareg,$nomeNegozio,$iva,$indirizzo,ECarta $carta,EAbbonamento $abb)
       {
           parent::__construct($name, $mail, $pw, $tel, $stato, $datareg);
           $this->nameShop=$nomeNegozio;
           $this->p_iva=$iva;
           $this->andress=$indirizzo;
           $this->card=new Ecarta($carta->getIntestat(),$carta->getNum(),$carta->getScad(),$carta->getCodcvv());
           $this->abbonamento=new EAbbonamento($abb->getData(),$abb->getImporto(),$abb->getStato());
       }


       //METODI GET

    /**
     * @return mixed
     */
    public function getNameShop()
    {
        return $this->nameShop;
    }

    /**
     * @return mixed
     */
    public function getPIva()
    {
        return $this->p_iva;
    }

    /**
     * @return mixed
     */
    public function getAndress()
    {
        return $this->andress;
    }

        //METODI SET

    /**
     * @param mixed $nameShop
     */
    public function setNameShop($nameShop)
    {
        $this->nameShop = $nameShop;
    }

    /**
     * @param mixed $p_iva
     */
    public function setPIva($p_iva)
    {
        $this->p_iva = $p_iva;
    }

    /**
     * @param mixed $andress
     */
    public function setAndress($andress)
    {
        $this->andress = $andress;
    }

    //Metodo toString

    public function toString()
    {
        return parent::toString(); // TODO: Change the autogenerated stub
        "Nome negozio: " . $this->nameShop . "\n";
        "Partita iva: " . $this->p_iva. "\n";
        "Indirizzo: " . $this->andress->toString(). "\n";
        "Carta: ".$this->card->toString()."\n";
        "Abbonamento: ".$this->abbonamento->toString()."\n";

    }






}